def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  value = num1 - num2
  value
end

def division(num1, num2)
  sum = num1 / num2
end

def multiplication(num1, num2)
  sum = num1 * num2
  sum
end

def modulo(num1, num2)
  sum = num1 % num2
  sum
end

def square_root(num)
  sum = Math.sqrt(num)
  sum
end